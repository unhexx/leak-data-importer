    def _parse_connections_section(self, content: str) -> list[dict]:
        """
        More advanced parser for ВОЗМОЖНЫЕ СВЯЗИ.

        Extracts richer information about related persons and classifies
        connection types more precisely (including family subtypes when possible).
        """
        connections: list[dict] = []
        if not content.strip():
            return connections

        lines = content.splitlines()
        current_connection: dict[str, Any] = {}

        family_keywords = {
            "мать": "parent", "отец": "parent", "мама": "parent", "папа": "parent",
            "сын": "child", "дочь": "child",
            "супруг": "spouse", "супруга": "spouse", "муж": "spouse", "жена": "spouse",
            "брат": "sibling", "сестра": "sibling",
        }

        connection_patterns = [
            (r"^(По ФИО|Родственники|По имени)", "relative"),
            (r"^(По адресу|Сожители|По прописке)", "address_cohabitant"),
            (r"^(По билету|Попутчики|По рейсу|Авиаперелеты)", "flight_companion"),
            (r"^(По телефону|По номеру)", "phone_contact"),
            (r"^(По документу|По паспорту)", "document_match"),
            (r"^(По ИНН|По СНИЛС)", "identifier_match"),
        ]

        for line in lines:
            stripped = line.strip()
            if not stripped:
                continue

            # Detect new connection header
            matched = False
            for pattern, default_type in connection_patterns:
                match = re.match(pattern, stripped, re.IGNORECASE)
                if match:
                    if current_connection and current_connection.get("related_fio"):
                        connections.append(current_connection)

                    related = stripped[match.end():].strip(" ·:-")

                    # Try to detect specific family relation
                    conn_type = default_type
                    for word, fam_type in family_keywords.items():
                        if word in stripped.lower():
                            conn_type = fam_type
                            break

                    current_connection = {
                        "connection_type": conn_type,
                        "related_fio": related,
                        "context": stripped,
                        "details": [],
                        "raw": stripped,
                    }
                    matched = True
                    break

            if not matched and current_connection:
                # Extract additional structured info about the related person
                if re.search(r"\d{2}\.\d{2}\.\d{4}", stripped):
                    current_connection["related_birth_date"] = normalize_date(stripped)

                if "паспорт" in stripped.lower():
                    current_connection.setdefault("related_documents", []).append(stripped)

                if stripped.startswith("·") or stripped.startswith("-") or ":" in stripped:
                    current_connection["details"].append(stripped)

        if current_connection and current_connection.get("related_fio"):
            connections.append(current_connection)

        return connections

    def _parse_border_events(self, content: str) -> list[dict]:
        """
        Significantly improved parser for ПЕРЕСЕЧЕНИЯ ГРАНИЦЫ.

        Extracts structured flight/border crossing events with:
        - Date/time
        - Flight number
        - Airline
        - Airports / direction
        - Passport used
        - Companions (if mentioned)
        """
        events: list[dict] = []
        if not content.strip():
            return events

        date_re = re.compile(r"(\d{2}\.\d{2}\.\d{4}(?:\s+\d{2}:\d{2}(?::\d{2})?)?)")
        flight_re = re.compile(r"(?:рейс|flight)\s*[:：]?\s*([A-Z0-9\-]+)", re.IGNORECASE)
        airline_re = re.compile(r"(?:авиакомпания|airline)\s*[:：]?\s*(.+?)(?:\s*\(|$)", re.IGNORECASE)

        current: dict[str, Any] = {}

        for line in content.splitlines():
            stripped = line.strip()
            if not stripped:
                continue

            # New event often starts with a date
            date_match = date_re.search(stripped)
            if date_match:
                if current and current.get("date"):
                    events.append(current)
                current = {
                    "event_type": "flight",
                    "date": normalize_datetime(date_match.group(1)),
                    "raw_lines": [stripped],
                    "details": {}
                }

            if not current:
                continue

            # Flight number
            fmatch = flight_re.search(stripped)
            if fmatch:
                current["flight_number"] = fmatch.group(1)

            # Airline
            amatch = airline_re.search(stripped)
            if amatch:
                current["airline"] = amatch.group(1).strip()

            # Passport
            if "паспорт" in stripped.lower():
                current["passport_used"] = stripped

            # Companions / poputchik - improved extraction
            if "попутчик" in stripped.lower() or "сопровождающий" in stripped.lower():
                companion = {"raw": stripped}
                # Try to extract FIO of companion
                fio_match = re.search(r"([А-ЯЁ][а-яё\-]+\s+[А-ЯЁ][а-яё\-]+(?:\s+[А-ЯЁ][а-яё\-]+)?)", stripped)
                if fio_match:
                    companion["fio"] = fio_match.group(1).strip()
                # Passport or document of companion
                if "паспорт" in stripped.lower():
                    companion["document"] = stripped
                current.setdefault("companions", []).append(companion)

            # General key:value
            if ":" in stripped:
                k, _, v = stripped.partition(":")
                current["details"][k.strip()] = v.strip()

            current["raw_lines"].append(stripped)

        if current and current.get("date"):
            events.append(current)

        return events

    def _parse_websites_section(self, content: str) -> list[str]:
        """
        Parse САЙТЫ, ГДЕ НАЙДЕНЫ РЕГИСТРАЦИИ section.
        Usually a simple list of domains/sites.
        """
        sites: list[str] = []
        if not content.strip():
            return sites

        for line in content.splitlines():
            stripped = line.strip()
            # Skip headers and empty lines
            if not stripped or stripped.startswith("САЙТЫ") or stripped.startswith("─"):
                continue
            # Basic domain-like strings
            if "." in stripped and len(stripped) > 3 and not stripped.startswith("·"):
                sites.append(stripped)

        return sites

    def _parse_sources_section(self, content: str) -> list[dict]:
        """
        Parse ИСТОЧНИКИ section according to spec:
        - Blocks start with "▼ Название источника (записей: N)"
        - Individual records inside are separated by long strings of middle dots (·)
        """
        findings: list[dict] = []

        if not content or "ИСТОЧНИКИ" not in content:
            return findings

        # Split by source headers
        source_blocks = re.split(r"\n(?=▼\s)", content)

        for block in source_blocks:
            if not block.strip().startswith("▼"):
                continue

            # Extract source name
            header_match = re.match(r"▼\s*(.+?)(?:\n|$)", block)
            source_name = header_match.group(1).strip() if header_match else "Unknown Source"

            # Split records by middle dot separator (U+00B7)
            record_separator = "·" * 10  # at least 10 middle dots
            records_raw = re.split(rf"\n\s*{record_separator}.*\n", block)

            for idx, rec in enumerate(records_raw):
                rec = rec.strip()
                if not rec or len(rec) < 10:
                    continue

                # Deep key-value extraction + normalization inside each source record
                data: dict[str, Any] = {}
                for line in rec.splitlines():
                    if ":" in line:
                        k, _, v = line.partition(":")
                        k = k.strip()
                        v = v.strip()
                        if not k or not v:
                            continue

                        k_lower = k.lower()

                        # Smart normalization per key type
                        if any(x in k_lower for x in ["тел", "моб", "phone"]):
                            norm = normalize_phone(v)
                            data[k] = norm or v
                        elif "дата" in k_lower:
                            data[k] = normalize_datetime(v) or v
                        elif any(x in k_lower for x in ["паспорт", "документ"]):
                            data[k] = normalize_passport(v) or v
                        elif "инн" in k_lower:
                            data[k] = normalize_inn(v) or v
                        elif "снилс" in k_lower:
                            data[k] = normalize_snils(v) or v
                        elif "email" in k_lower or "почта" in k_lower:
                            data[k] = v.lower()
                        else:
                            data[k] = v

                findings.append({
                    "source_name": source_name,
                    "record_index": idx,
                    "data": data,
                    "raw_text": rec[:2500],
                })

        return findings

    # ==================== POST-PROCESSING ====================

    def _post_process(self, report: ParsedReport) -> None:
        """
        Fill main_* fields in main_person using frequency analysis + deduplication.
        This runs after all sections have been parsed.
        """
        person = report.main_person

        # Collect candidates from everywhere
        all_phones: list[str] = list(person.phones) + list(person.extra_phones)
        all_emails: list[str] = list(person.emails) + list(person.extra_emails)
        all_passports: list[str] = list(person.passports) + list(person.extra_passports)
        all_inns: list[str] = list(person.extra_inns)
        if person.inn:
            all_inns.append(person.inn)

        # From documents
        for doc in report.documents:
            num = doc.get("number") or doc.get("number_raw", "")
            if num:
                p = normalize_passport(num)
                if p:
                    all_passports.append(p)
                inn = normalize_inn(num)
                if inn:
                    all_inns.append(inn)

        # From source findings (richest source)
        for finding in report.source_findings:
            data = finding.get("data", {})
            for key, val in data.items():
                k = key.lower()
                if any(x in k for x in ["тел", "моб", "phone"]):
                    p = normalize_phone(val)
                    if p: all_phones.append(p)
                elif "email" in k or "почта" in k:
                    all_emails.append(val.lower())
                elif "паспорт" in k:
                    p = normalize_passport(val)
                    if p: all_passports.append(p)
                elif "инн" in k:
                    i = normalize_inn(val)
                    if i: all_inns.append(i)
                elif "снилс" in k and not person.snils:
                    person.snils = normalize_snils(val)

        def most_common(items: list[str]) -> Optional[str]:
            if not items:
                return None
            from collections import Counter
            cleaned = [x for x in items if x]
            return Counter(cleaned).most_common(1)[0][0] if cleaned else None

        # Fill main fields preferring the most frequent values
        person.main_phone = person.main_phone or most_common(all_phones)
        person.main_email = person.main_email or most_common(all_emails)
        person.main_passport = person.main_passport or most_common(all_passports)
        person.main_inn = person.main_inn or most_common(all_inns)

        # Final deduplication
        person.phones = sorted(set(all_phones))
        person.emails = sorted(set(all_emails))
        person.passports = sorted(set(all_passports))
        person.extra_phones = sorted(set(person.extra_phones))
        person.extra_emails = sorted(set(person.extra_emails))
        person.extra_passports = sorted(set(person.extra_passports))
        person.extra_inns = sorted(set(all_inns))
